#include "../../includes/minishell.h"

void	ft_clean_env(t_shell *shell)
{
	char	*tmp_char;
	int		i;

	i = 0;
	while (shell->env[i])
	{
		free(shell->env[i]);
		i++;
	}
	if (shell->env)
		free(shell->env);
}